public class YearMonth {

    private int y;

    private int m;

    public YearMonth(int y, int m) {
        this.y = y;
        this.m = m;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getM() {
        return m;
    }

    public void setM(int m) {
        this.m = m;
    }
}
